import { gql } from 'apollo-boost';

// ---------------------------------------------------------------------------------- //
// ---------------------------------------------------------------------------------- //


export const LocationSearch = gql`
    mutation SearchForLocation($location: String!) {
        search(term: "matcha", location: $location, limit: 10) {
            total
            business {
                name
                id
                alias
                rating
                url
            }
        }
    }
`;

