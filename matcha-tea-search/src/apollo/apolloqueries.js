import gql from 'graphql-tag';

// ---------------------------------------------------------------------------------- //
// ---------------------------------------------------------------------------------- //


export const LOCATION_SEARCH = gql`
    query LocationSearch($location: String!, $radius: Float!, $limit: Int!) {
        search(term: "matcha", location: $location, radius: $radius, limit: $limit) {
            total
            business {
                name
                id
                alias
                rating
                url
            }
        }
    }
`;

