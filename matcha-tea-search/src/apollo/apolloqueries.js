import gql from 'graphql-tag';

// ---------------------------------------------------------------------------------- //
// ---------------------------------------------------------------------------------- //


export const LOCATION_SEARCH = gql`
    query LocationSearch($location: String!, $radius: Float!) {
        search(term: "matcha", location: $location, limit: 5, radius: $radius) {
            total
            business {
                name
                id
                alias
                rating
                url
            }
        }
    }
`;

